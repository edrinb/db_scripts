SELECT 'TABLE',T.TABLE_NAME, O.CREATED, US.BYTES/(1024*1024) 
FROM DBA_TABLES T, DBA_OBJECTS O, DBA_SEGMENTS US
WHERE T.TABLE_NAME = O.OBJECT_NAME
AND O.OBJECT_TYPE = 'TABLE'
AND T.TABLE_NAME = US.segment_name
AND T.owner = 'BSCM'
AND TRUNC(O.CREATED) not in ('20-Aug-2010', '18-Jul-2010','17-Mar-2012', '18-Mar-2012', '31-Jan-2013')--('15-Jun-2010', '23-Jun-2010')
UNION
SELECT 'INDEX', UI.index_name, O1.CREATED, US.BYTES/(1024*1024)
FROM DBA_INDEXES UI, DBA_SEGMENTS US, DBA_OBJECTS O1
WHERE UI.table_name IN (SELECT TABLE_NAME FROM DBA_TABLES T, DBA_OBJECTS O WHERE T.TABLE_NAME = O.OBJECT_NAME
                        AND O.OBJECT_TYPE = 'TABLE' AND T.owner = 'BSCM' AND TRUNC(O.CREATED) not in ('20-Aug-2010', '18-Jul-2010','17-Mar-2012', '18-Mar-2012', '31-Jan-2013'))
AND UI.index_name = US.segment_name
AND UI.index_name = O1.OBJECT_NAME
ORDER BY 1, 2


